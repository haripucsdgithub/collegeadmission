package com.example.demo.controlstructures.interative;

import java.util.Scanner;

public class ForLoop {

    public static void main(String args[]) {
        int n;
        Scanner s = new Scanner(System.in);
         n = s.nextInt();
        int i;
        for ( i=0; i<n; System.out.println("Hello") ) {

            i++;
        }
    }
}
