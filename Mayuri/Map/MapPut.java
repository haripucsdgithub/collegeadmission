package Map;

import java.util.HashMap;

public class MapPut {
    public static void main(String[] args) {

        HashMap <Integer, String> map = new HashMap<>();

        map.put(1, "I");
        map.put(2, "AM");
        map.put(3, "A");
        map.put(4, "GOOD");
        map.put(5, "GIRL");

        System.out.println(map);

    }
}
